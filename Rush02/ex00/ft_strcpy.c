#include "rush02.h"

char	*ft_strcpy(char *dest, char *src)
{
	int	z;

	z = 0;
	while (src[z] != '\0')
	{
		dest[z] = src[z];
		z++;
	}
	dest[z] = '\0';
	return (dest);
}
