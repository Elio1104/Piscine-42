#include <unistd.h>

void	putchar(char c)
{
	write (1, &c, 1);
}

void	display(int tab[6][6])
{
	int	i;
	int	j;

	i = 1;
	while (i < 5)
	{
		j = 1;
		while (j < 5)
		{
			putchar(tab[j][i]);
			putchar(' ');
			j++;
		}
		putchar('\n');
		i++;
	}
}
